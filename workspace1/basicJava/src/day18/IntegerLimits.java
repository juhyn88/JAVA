package day18;

public class IntegerLimits {

	public static void main(String[] args) {
		System.out.println("정수형의 표현 범위");
		System.out.println("byte 형(1byte):" + Byte.MIN_VALUE + " ~ " + Byte.MAX_VALUE);
		System.out.println("short 형(2byte):" + Short.MIN_VALUE + " ~ " + Short.MAX_VALUE);
		System.out.println("int 형(4byte):" + Integer.MIN_VALUE + " ~ " + Integer.MAX_VALUE);
		System.out.println("long 형(8byte):" + Long.MIN_VALUE + " ~ " + Long.MAX_VALUE);
		

	}

}
